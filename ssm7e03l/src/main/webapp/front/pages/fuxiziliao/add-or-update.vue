<script>
	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				ruleForm: {
												ziliaomingcheng: '',
																kemu: '',
																tupian: '',
																ziliaoshipin: '',
																faburiqi: '',
																xiangqing: '',
																clicktime: '',
																clicknum: '',
												},
																				kemuOptions: [],
				kemuIndex: 0,
																																																												// 登陆用户信息
				user: {},
			}
		},
		components: {
			wPicker
		},
		computed: {
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
					},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
						// ss读取
																																																						
																		// 下拉框
			res = await this.$api.option(`kemu`,`kemu`,{});
			this.kemuOptions = res.data;
																																													
			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`fuxiziliao`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
															if(o=='ziliaomingcheng'){
					this.ruleForm.ziliaomingcheng = obj[o];
					continue;
					}
																				if(o=='kemu'){
					this.ruleForm.kemu = obj[o];
					continue;
					}
																				if(o=='tupian'){
					this.ruleForm.tupian = obj[o];
					continue;
					}
																				if(o=='ziliaoshipin'){
					this.ruleForm.ziliaoshipin = obj[o];
					continue;
					}
																				if(o=='faburiqi'){
					this.ruleForm.faburiqi = obj[o];
					continue;
					}
																				if(o=='xiangqing'){
					this.ruleForm.xiangqing = obj[o];
					continue;
					}
																				if(o=='clicktime'){
					this.ruleForm.clicktime = obj[o];
					continue;
					}
																				if(o=='clicknum'){
					this.ruleForm.clicknum = obj[o];
					continue;
					}
														}
																																																																							}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
																																																						
			// 多级联动参数
																																																						
																																	faburiqiChange(e) {
				this.ruleForm.faburiqi = e.target.value;
				this.$forceUpdate();
			},
																								
																																													// 日长控件选择日期时间
			clicktimeConfirm(val) {
				console.log(val)
				this.ruleForm.clicktime = val.result;
				this.$forceUpdate();
			},
												
															// 下拉变化
			kemuChange(e) {
				this.kemuIndex = e.target.value
				this.ruleForm.kemu = this.kemuOptions[this.kemuIndex]
			},
																																										
																											tupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.tupian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},
															ziliaoshipinTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.ziliaoshipin = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},
																																													
			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
																																																																																																																																																																																																																																																																																																																												if((!this.ruleForm.ziliaomingcheng)){
					this.$utils.msg(`资料名称不能为空`);
					return
				}
																																																																																																																																																																																																																																												if(this.ruleForm.clicknum&&(!this.$validate.isIntNumer(this.ruleForm.clicknum))){
					this.$utils.msg(`点击次数应输入整数`);
					return
				}
																																if(this.ruleForm.id){
					await this.$api.update(`fuxiziliao`, this.ruleForm);
				}else{
					await this.$api.add(`fuxiziliao`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

